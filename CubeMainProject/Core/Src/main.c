/**
  * @brief  Function implementing the EepromTask thread.
  * @param  argument: Not used
  * @retval None
  */
/* USER CODE END Header_StartEepromTask */
void StartEepromTask(void const * argument)
{
  /* USER CODE BEGIN 5 */
  NvM_Block_t block_type;

  /* Infinite loop */
  for(;;)
  {
	  if (xQueueReceive(EepromQueueHandle,
			  &block_type,
			  (TickType_t) 100) == pdPASS)
	  {
		  CLI_Send_Sync("NvM: 1", sizeof("NvM: 1"));

		  switch (block_type)
		  {
		      case NvM_Block_RTC:
		      {
		    	  NvM_Save_RTC();
		    	  break;
		      }
          case NvM_Block_Display:
          {
          NvM_Save_Display();
          break;
          }
          case NvM_Block_System:
          {
            NvM_Save_System();
            break;
          }
		      default:
		      {
		    	  break;
		      }
      }

		  /* After each block that should be saved --> calculate crc and save it also in NvM. */
		  nvm_ram.validity.crc = NvM_CalculateCRC();
		  NvM_Save_Validity();

		  CLI_Send_Sync("NvM: 0", sizeof("NvM: 0"));
	  }
  }
  /* USER CODE END 5 */
}

